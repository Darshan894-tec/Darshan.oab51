package ProgramsPractices;

public class Staticclass 
{
	static void add()
	
	{
	int a=10;
	int b=20;
	int resut=a+b;
	System.out.println(resut);
	}
	
	static void sub()
	{
		int a=34;
		int b=45;
		int result1=a-b;
		System.out.println(result1);
	}
	static void mul()
	{
		int a=34;
		int b=34;
		int result2=a*b;
		System.out.println(result2);
	}
	
	public static void main(String[] args) 
	{
		
		add();
		sub();
		mul();
	}

}

package ProgramsPractices;

public class Loops2 
{
	public static void main(String[] args) 
	{
		int age[]= {10,20,30,40};
		
		for (int i=0;i<age.length; i++ )
			
		{
			
			if (age[i]>=18)
			{
				System.out.println("He/She can vote");
			}
			else
			{
				System.out.println("He/She cant vote");
				
			}
			
		}
		
		
	}

}
